package com.zhuanjingkj.zjcbe.commondata.rto;

public abstract class BaseRTO {
    protected String p;
    protected String v;

    public String getP() {
        return p;
    }

    public void setP(String p) {
        this.p = p;
    }

    public String getV() {
        return v;
    }

    public void setV(String v) {
        this.v = v;
    }
}
